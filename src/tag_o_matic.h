#include "mfrc522_i2c.h"
#include <Wire.h>

class TagOMatic {
public:
	enum RFID_State {
    READ_MODE,
		WRITE_MODE,
    LOAD_MODE,
    SAVE_MODE
  };

  MFRC522 mfrc522 = MFRC522(0x28);


	/////////////////////////////////////////////////////////////////////////////////////
	// Constructor
	/////////////////////////////////////////////////////////////////////////////////////
	TagOMatic();

	/////////////////////////////////////////////////////////////////////////////////////
	// Arduino Life Cycle
	/////////////////////////////////////////////////////////////////////////////////////
	void setup();
	void loop();

private:
	bool _read_uid = false;
  RFID_State current_state;
	MFRC522::Uid uid;

	/////////////////////////////////////////////////////////////////////////////////////
	// Display functions
	/////////////////////////////////////////////////////////////////////////////////////
  void cls();
  void display_banner();
	void dump_card_details();

	/////////////////////////////////////////////////////////////////////////////////////
	// State management
	/////////////////////////////////////////////////////////////////////////////////////
	void select_state();
  void set_state(RFID_State state);

	/////////////////////////////////////////////////////////////////////////////////////
	// Operations
	/////////////////////////////////////////////////////////////////////////////////////
	void read_card();
	void write_card();
	void save_uid();
	void load_uid();

	/////////////////////////////////////////////////////////////////////////////////////
	// Helpers
	/////////////////////////////////////////////////////////////////////////////////////
  bool write_file(String filename, String uid_str);
  bool load_from_file();
  String get_string_uid(MFRC522::Uid *_uid);
};
